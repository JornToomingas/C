// 3. Basic Math
// - Create two integers (for example: a and b).
// - Print their sum.
// - Print their difference.
// - Print their product.
// - Print their division.

#include <stdio.h>

int a = 23;
int b = 10;

int main() {
    printf("%d\n", a + b);
    printf("%d\n", a - b);
    printf("%d\n", a * b);
    printf("%d\n", a / b);
    return 0;
}